// This is a simple Java program that prints "Hello World" to the console.
// It demonstrates the basic structure of a Java program.

public class basicSyntax {
    // The name of the class is "basicSyntax".
    // In Java, every program must have at least one class, and the file name
    // should match the class name (with the `.java` extension).

    public static void main(String[] args) {
        // The "main" method is the entry point of any Java program.
        // When you run the program, the code inside this method is executed.
        // Let's break down the components:
        // - public: This means the method can be accessed from outside the class.
        // - static: This means the method belongs to the class itself, not an object of
        // the class.
        // - void: This means the method does not return any value.
        // - String[] args: This is an array of strings used to pass arguments to the
        // program (optional).

        System.out.println("Hello World");
        // This line prints "Hello World" to the console.
        // - System: A built-in Java class that provides access to the system (e.g.,
        // console, input, output).
        // - out: A static member of the System class, representing the output stream
        // (console).
        // - println: A method used to print text to the console, followed by a new
        // line.

    }
    // End of the main method.
}
// End of the class.
